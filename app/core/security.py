"""security — утилиты для секретов и токенов сервиса."""

import hashlib


def hash_api_token(token: str) -> str:
    """Возвращает SHA-256 хеш токена в hex-строке (64 символа).

    Хранение хеша вместо открытого токена защищает от утечки доступа при
    дампе БД или попадании логов в чужие руки. Сравнение по unique-индексу
    хеша работает так же быстро, как по plain-токену.
    """

    return hashlib.sha256(token.encode("utf-8")).hexdigest()
